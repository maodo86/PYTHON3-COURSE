# Variables

var1 = 'Ali'
var2 = 100.100
var3 = 22
var4 = 188.


if __name__ == '__main__':
    # display all values of all variables
    print(var1)
    print(var2)
    print(var3)
    print(var4)

    # display all values of all variables inline way
    print(var1, var2, var3, var4)
    print(var1, var2, var3, var4, sep=' ')

    # display all values of all variables with endline effect
    print(var1, var2, var3, var4, sep='\n')
