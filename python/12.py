# Tuple (Part 3)
# usual operations on tuple

var1 = (1, 2, 3)
var2 = (4, 5, 6)

var3 = ('Esmt', 'Ecole', 'Supérieur')
var4 = ('Multinationale', 'des', 'Télécommunications')


if __name__ == '__main__':
    # added var1 and var2
    added1 = var1 + var2
    added2 = var3 + var4

    print(added1)
    print('\n')

    print(added2)
    print('\n')

    # multiply var1, var2
    mult1 = var1 * 3
    mult2 = var2 * 4

    print(mult1)
    print('\n')
    print(mult2)
    print('\n')
